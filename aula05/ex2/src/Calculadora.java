public class Calculadora {
    double numero1;
    double numero2;

    double somar(){
        double resultado= numero1+numero2;
        return resultado;
    }

    double multiplicar(){
        double resultado= numero1*numero2;
        return resultado;
    }

    double dividir (){
        double resultado= numero1/numero2;
        return resultado;
    }

    double restar(){
        double resultado= numero1-numero2;
        return resultado;
    }
}
