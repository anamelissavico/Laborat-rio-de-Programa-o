public class Fracao {
    int numerador;
    int denominador;

    Fracao multiplica_funcao (Fracao fracao){

        Fracao fracaoNova= new Fracao();
        fracaoNova.numerador= fracao.numerador*numerador;
        fracaoNova.denominador=  fracao.denominador*denominador;
        return fracaoNova;
    }

}